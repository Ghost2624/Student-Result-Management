    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String rollno = (String)jTextField1.getText();
        String em = (String)jTextField2.getText();
        String dbms = (String)jTextField3.getText();
        String os = (String)jTextField4.getText();
        String maths = (String)jTextField5.getText();
        String ml = (String)jTextField6.getText();
        
        try{
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Abhi568");
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("select * from student where rollno= '"+rollno+"'");
             if(rs.next())
             {
                 st.executeUpdate("insert into result values('"+rollno+"','"+em+"','"+dbms+"','"+os+"','"+maths+"','"+ml+"')");
                 JOptionPane.showMessageDialog(null, "Data Saved Successfully");
                 setVisible(false);
                 new insertResult().setVisible(true);
             }
             else
             {
                 JOptionPane.showMessageDialog(null,"Roll Number does not Exist");
             }
             
         }
         catch(Exception e)
         {
             JOptionPane.showMessageDialog(null, e.toString());
         }
    }//GEN-LAST:event_jButton6ActionPerformed

