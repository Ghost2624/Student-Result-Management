    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String rollno = (String)jTextField2.getText();
        String name = (String)jTextField1.getText();
        String gender = (String)jComboBox1.getSelectedItem();
        String cName = (String)jComboBox2.getSelectedItem();
        String bName = (String)jComboBox3.getSelectedItem();
        

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Abhi568");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno= '"+rollno+"'");
            if(rs.next())
            {
                st.executeUpdate("update student set name = '"+name+"', coursename = '"+cName+"',branchname ='"+bName+"',gender = '"+gender+"' where rollno ='"+rollno+"'");
                JOptionPane.showMessageDialog(null, "Data updated Successfully");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Roll Number does not Exist");
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

